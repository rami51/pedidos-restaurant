package com.utn.rjmg.pedrest.specification.personas;

public class ClienteFilter {
	private String filterName;
	private String filterSurname;
	private String filterCompany;

	public String getFilterName() {
		return filterName;	}

	public void setFilterName(String filterName) {
		this.filterName = filterName;	}

	public String getFilterSurname() {
		return filterSurname;	}

	public void setFilterSurname(String filterSurname) {
		this.filterSurname = filterSurname;	}

	public String getFilterCompany() {
		return filterCompany;
	}

	public void setFilterCompany(String filterCompany) {
		this.filterCompany = filterCompany;
	}

	
}
