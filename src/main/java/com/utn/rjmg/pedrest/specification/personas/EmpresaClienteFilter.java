package com.utn.rjmg.pedrest.specification.personas;

public class EmpresaClienteFilter {
	private String filterName;
	private String filterCuit;
	private String filterEmail;
	public String getFilterName() {
		return filterName;
	}
	public void setFilterName(String filterName) {
		this.filterName = filterName;
	}
	public String getFilterCuit() {
		return filterCuit;
	}
	public void setFilterCuit(String filterCuit) {
		this.filterCuit = filterCuit;
	}
	public String getFilterEmail() {
		return filterEmail;
	}
	public void setFilterEmail(String filterEmail) {
		this.filterEmail = filterEmail;
	}

	

	
}
