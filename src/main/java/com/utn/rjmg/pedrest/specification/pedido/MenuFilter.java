package com.utn.rjmg.pedrest.specification.pedido;

public class MenuFilter {
private String filterName;
private String filterEmpresa;
private String filterEstado;
private String filterHorario;
public String getFilterName() {
	return filterName;
}
public void setFilterName(String filterName) {
	this.filterName = filterName;
}
public String getFilterEmpresa() {
	return filterEmpresa;
}
public void setFilterEmpresa(String filterEmpresa) {
	this.filterEmpresa = filterEmpresa;
}
public String getFilterEstado() {
	return filterEstado;
}
public void setFilterEstado(String filterEstado) {
	this.filterEstado = filterEstado;
}
public String getFilterHorario() {
	return filterHorario;
}
public void setFilterHorario(String filterHorario) {
	this.filterHorario = filterHorario;
}

	
}
