package com.utn.rjmg.pedrest.specification.pedido;

public class MesaFilter {
private String filterName;
private String filterSalsa;
private String filterHorno;
private String filterGuarnicion;


public String getFilterName() {
	return filterName;
}

public void setFilterName(String filterName) {
	this.filterName = filterName;
}

public String getFilterSalsa() {
	return filterSalsa;
}

public void setFilterSalsa(String filterSalsa) {
	this.filterSalsa = filterSalsa;
}

public String getFilterHorno() {
	return filterHorno;
}

public void setFilterHorno(String filterHorno) {
	this.filterHorno = filterHorno;
}

public String getFilterGuarnicion() {
	return filterGuarnicion;
}

public void setFilterGuarnicion(String filterGuarnicion) {
	this.filterGuarnicion = filterGuarnicion;
}	


}
