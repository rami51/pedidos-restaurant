package com.utn.rjmg.pedrest.specification.usuario;

public class UsuarioFilter {
	private String filterName;
	private String filterTipoUsuario;

	public String getFilterName() {
		return filterName;
	}

	public void setFilterName(String filterName) {
		this.filterName = filterName;
	}

	public String getFilterTipoUsuario() {
		return filterTipoUsuario;
	}

	public void setFilterTipoUsuario(String filterTipoUsuario) {
		this.filterTipoUsuario = filterTipoUsuario;
	}

	
}
